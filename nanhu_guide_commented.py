    """
    打印菜单区域标题
    参数：
        标题 - 要显示的标题文字（前面会自动加📌图标）
    先打印空行，再输出标题，再打印分隔线
    """
    print()
    print(f"  📌 {标题}")
    打印分隔线()   # 调用分隔线函数


