def 清屏():
    """
    清除控制台屏幕
    根据操作系统自动选择命令：Windows 用 cls，Linux/Mac 用 clear
    """
    os.system('cls' if os.name == 'nt' else 'clear')
    # os.name == 'nt' 表示 Windows 系统
    # 其他系统（'posix'）用 clear


