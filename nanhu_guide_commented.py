def 打印分隔线(字符="─", 长度=56):
    """
    打印水平分隔线
    参数：
        字符 - 用于分隔线的字符（默认为'─'）
        长度 - 分隔线的长度（默认为56个字符）
    """
    print(f"  {字符 * 长度}")   # 缩进2格后重复打印指定字符


