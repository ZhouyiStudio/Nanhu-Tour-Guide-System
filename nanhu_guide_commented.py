def 列出所有景点():
    """
    列出所有景点，按类别分组展示
    用户可以通过输入编号查看景点详情
    流程：清屏 → 标题 → 按类别分组 → 列出各景点 → 等待用户选择编号
    """
    清屏()
    打印标题()
    打印菜单标题("南湖景点一览")
    
    # 按类别分组
    分类 = {}                       # 创建空字典，键是类别名，值是该类别的景点列表
    for 景点 in 景点列表:            # 遍历所有景点
        类别名 = 景点["category"]     # 获取景点类别
        if 类别名 not in 分类:       # 如果该类别还没有添加到字典中
            分类[类别名] = []        # 初始化该类别对应的空列表
        分类[类别名].append(景点)    # 将当前景点添加到对应类别列表中
    
    # 遍历每个类别，打印类别名和该类别下的所有景点
    for 类别名, 该类景点 in 分类.items():
        print(f"\n  ▸ {类别名}")     # 打印类别名
        for 景点 in 该类景点:         # 遍历该类别下的每个景点
            print(f"    [{景点['id']}] {景点['name']}  {景点['rating']}")  # 打印编号、名称、评分
    
    print()
    打印分隔线()
    
    # 循环等待用户选择景点编号
    while True:
        try:
            选择 = input("  请输入景点编号查看详情（0返回主菜单）：")  # 获取用户输入
            if 选择 == "0":        # 如果输入0则返回主菜单
                return
            景点编号 = int(选择)    # 将输入转为整数（可能是景点编号）
            景点 = next((s for s in 景点列表 if s["id"] == 景点编号), None)  # 在景点列表中查找匹配的景点
            if 景点:                # 如果找到了对应的景点
                显示景点详情(景点)   # 调用函数显示景点详情
                等待回车()          # 等待用户看完按回车
            else:
                print("  ❌ 无效编号，请重新输入。")  # 编号不在列表中
                continue
        except ValueError:          # 如果输入不是有效数字
            print("  ❌ 请输入数字编号。")
            continue


def 搜索景点():
    """
    按关键词搜索景点
    用户输入关键词（可以是名称、类别或描述中的任意部分）
    显示匹配到的景点列表，用户可选择编号查看详情
    """
    清屏()
    打印标题()
    打印菜单标题("🔍 搜索景点")
    
    # 获取用户输入的关键词，去掉首尾空格
    关键词 = input("  请输入关键词（名称/类别/描述）：").strip()
    if not 关键词:                   # 如果关键词为空
        print("  未输入关键词。")
        等待回车()
        return
    
    搜索结果 = []                    # 存放匹配到的景点
    for 景点 in 景点列表:            # 遍历所有景点进行匹配
        # 检查关键词是否出现在景点名称、类别或描述中
        if (关键词 in 景点["name"] or 
            关键词 in 景点["category"] or 
            关键词 in 景点["desc"]):
            搜索结果.append(景点)    # 匹配成功则加入结果列表
    
    if not 搜索结果:                 # 如果没有任何匹配
        print(f"\n  未找到包含{关键词}的景点。")
        等待回车()
        return
    
    # 显示搜索结果
    print(f"\n  找到 {len(搜索结果)} 个相关景点：")
    for 景点 in 搜索结果:
        print(f"  [{景点['id']}] {景点['name']} - {景点['category']}")
        print(f"        {景点['desc'][:50]}...")  # 只显示描述的前50个字符
    
    print()
    # 循环让用户选择查看详情
    while True:
        try:
            选择 = input("  输入编号查看详情（0返回）：")
            if 选择 == "0":
                return
            景点编号 = int(选择)
            景点 = next((s for s in 搜索结果 if s["id"] == 景点编号), None)
            if 景点:
                显示景点详情(景点)
                等待回车()
                return
            else:
                print("  ❌ 无效编号。")
        except ValueError:
            print("  ❌ 请输入数字。")


